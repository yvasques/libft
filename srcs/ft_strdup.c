#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*dest;
	size_t	i;
	size_t	len;

	len = ft_strlen(str);
	dest = (char *) malloc(sizeof(char) * (len + 1));
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (str[i])
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
